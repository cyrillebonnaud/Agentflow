'use strict';

/**
 * Run the quick-prototype flow for "a quiz app" with a fake subprocess runner
 * so the full orchestrator pipeline executes without real Claude invocations.
 *
 * Usage: node scripts/run-quick-proto.js
 */

const path = require('node:path');
const fs = require('node:fs/promises');
const os = require('node:os');
const crypto = require('node:crypto');
const yaml = require('js-yaml');

const { runFlow } = require('../src/core/orchestrator');
const runner = require('../src/subprocess/runner');

const FLOW_INPUT = 'A quiz app — users create and take quizzes on any topic, with scoring and leaderboards';

/** Fake subprocess: writes realistic quiz-app content for each step. */
const FAKE_OUTPUTS = {
  'mini-prd': `# Mini-PRD: Quiz App

## Problem
People want a fun, lightweight way to create and share quizzes with friends or colleagues.

## Target Users
- Casual learners wanting to test knowledge
- Teachers creating classroom quizzes
- Teams running trivia games

## Core Features
1. Quiz creation (multiple choice, true/false)
2. Shareable quiz links
3. Real-time scoring
4. Leaderboard per quiz

## Out of Scope (v1)
- Native mobile app
- Monetisation
- Analytics dashboard
`,
  'ux-brief': `# UX Brief: Quiz App

## User Goals
- Create a quiz in < 5 minutes
- Share it via a single link
- See live results without refreshing

## Key Flows
1. **Create**: name quiz → add questions → publish
2. **Take**: open link → answer → see score
3. **Leaderboard**: auto-updated after each submission

## UX Principles
- Zero sign-up required to take a quiz
- Mobile-first layout
- Instant feedback after each question
`,
  'ux-directions': `# UX Directions: Quiz App

## Direction A — Clean & Minimal
Single-column layout, large tap targets, progress bar at top.
Focus on speed: swipe to next question.

## Direction B — Game-Like
Card-flip animations, countdown timer, confetti on correct answer.
Leaderboard visible as a side panel during quiz.

## Direction C — Collaborative Classroom
Split view: teacher dashboard on the left, live student responses on the right.
Real-time bar chart updating as students answer.
`,
};

function fakeContentForStep(sentinelDir) {
  for (const [stepId, content] of Object.entries(FAKE_OUTPUTS)) {
    if (sentinelDir.includes(stepId)) return content;
  }
  return '# Generated output\n\nContent generated by fake agent.';
}

async function main() {
  const tmpDir = path.join(os.tmpdir(), `af-quiz-${crypto.randomBytes(4).toString('hex')}`);
  const agentsDir = path.join(tmpDir, 'agents');
  const runsDir = path.join(tmpDir, 'runs');

  await fs.mkdir(agentsDir, { recursive: true });
  await fs.mkdir(runsDir, { recursive: true });

  // Create fake agent files
  for (const id of ['product-manager', 'ux-designer']) {
    await fs.writeFile(path.join(agentsDir, `${id}.md`), `# ${id}\nI am the ${id} agent.`);
  }

  // Build registry pointing to our fake agents
  const registry = {
    agents: new Map([
      ['product-manager', { path: path.join(agentsDir, 'product-manager.md'), plugin: 'test' }],
      ['ux-designer', { path: path.join(agentsDir, 'ux-designer.md'), plugin: 'test' }],
    ]),
    skills: new Map(),
    context: new Map(),
    templates: new Map(),
  };

  // Monkey-patch runner to write fake outputs instead of spawning claude
  const savedSpawn = runner.spawnSubprocess;
  runner.spawnSubprocess = async ({ sentinelDir }) => {
    const content = fakeContentForStep(sentinelDir);
    await fs.writeFile(path.join(sentinelDir, 'output.md'), content, 'utf8');
    await fs.writeFile(
      path.join(sentinelDir, '.done'),
      JSON.stringify({ result: content, cost_usd: 0 }),
      'utf8'
    );
  };

  const flowFile = path.resolve(__dirname, '../flows/quick-prototype.yaml');

  console.log('=== Agentflow: quick-prototype ===');
  console.log(`Input: "${FLOW_INPUT}"\n`);

  try {
    const { runId, runDir, runState } = await runFlow({
      flowFile,
      flowInput: FLOW_INPUT,
      runsDir,
      registry,
      config: { subprocessTimeoutMs: 15000, watchdogPollIntervalMs: 1000 },
    });

    console.log(`Run ID : ${runId}`);
    console.log(`Status : ${runState.status}\n`);
    console.log('Steps:');
    for (const [stepId, step] of Object.entries(runState.steps)) {
      const icon = step.status === 'done' ? '✓' : step.status === 'skipped' ? '→' : '✗';
      console.log(`  ${icon} ${stepId}: ${step.status}`);
    }

    console.log('\nArtifacts:');
    const artifactsDir = path.join(runDir, 'artifacts');
    async function listArtifacts(dir, prefix = '') {
      const entries = await fs.readdir(dir, { withFileTypes: true });
      for (const entry of entries) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) {
          console.log(`  ${prefix}${entry.name}/`);
          await listArtifacts(full, prefix + '  ');
        } else {
          const content = await fs.readFile(full, 'utf8');
          const preview = content.split('\n').slice(0, 3).join(' ').substring(0, 80);
          console.log(`  ${prefix}${entry.name}  →  ${preview}…`);
        }
      }
    }
    await listArtifacts(artifactsDir);

  } finally {
    runner.spawnSubprocess = savedSpawn;
    await fs.rm(tmpDir, { recursive: true, force: true });
  }
}

main().catch(err => {
  console.error('Error:', err.message);
  process.exit(1);
});
